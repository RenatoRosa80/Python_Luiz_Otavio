"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""
contador = 10

###

contador /= 5
print(contador)


#-------------------------

contador +=2
print(contador)

#------------------
contador *= 2
print(contador)

#-----------------

contador %= 2
print(contador)

#--------------
contador /= 5
print(contador)